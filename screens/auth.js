/*
Tela de Cadastro/Login

- Opções para Criar Conta:

    Título: Um título claro e acolhedor, como "Crie sua Conta" ou "Bem-vindo ao FootUp! Cadastre-se agora".

    Formulário de Cadastro:

        Campo de E-mail: Um campo para o usuário inserir seu endereço de e-mail. Deve incluir um texto de ajuda, como "Insira seu e-mail para criar uma conta".

        Campo de Senha: Um campo para o usuário definir sua senha. É importante incluir uma opção de "Mostrar/Ocultar Senha" para facilitar a digitação.

        Confirmação de Senha: Um campo adicional para o usuário repetir a senha e garantir que foi digitada corretamente, acompanhado de um texto como "Confirme sua senha".

        Botão de Cadastro: Um botão grande e chamativo que diz "Criar Conta" ou "Cadastrar", que, ao ser clicado, valida os campos e cria a conta.

        Opções de Redes Sociais: Botões para cadastro rápido usando redes sociais populares (por exemplo, Google, Facebook, Twitter). Cada botão deve ter um ícone correspondente e um texto, como "Cadastre-se com Google".

        Termos de Uso e Política de Privacidade: Uma caixa de seleção que o usuário deve marcar, confirmando que leu e concorda com os Termos de Uso e a Política de Privacidade do aplicativo. Um link deve estar disponível para que o usuário possa acessá-los.

- Tela de Login para Usuários Existentes:

    Título: Um título como "Já possui uma conta? Faça login".

    Formulário de Login:

        Campo de E-mail: Um campo para o usuário inserir seu e-mail, semelhante ao campo do cadastro, com um texto de ajuda como "Insira seu e-mail para login".

        Campo de Senha: Um campo para a senha, com a opção de "Mostrar/Ocultar Senha".

        Botão de Login: Um botão destacado que diz "Login" ou "Entrar", que, ao ser clicado, autentica o usuário.

        Recuperação de Senha: Um link que diz "Esqueceu sua senha?" que leva a uma tela para recuperação de senha, onde o usuário pode inserir seu e-mail para receber instruções de redefinição.

        Alternativa de Cadastro: Um texto como "Não tem uma conta? Cadastre-se" que leva o usuário de volta à tela de cadastro, incentivando novos usuários a se registrarem.

- Layout Sugerido

01.  Cabeçalho: Logo do aplicativo e título "Cadastro/Login".

02. Seção de Cadastro:
    02.1. Título: "Crie sua Conta".
    02.2 Campos de E-mail, Senha e Confirmação de Senha.
    02.3 Botão de Cadastro.
    02.4 Botões de redes sociais.
    02.5 Caixa de seleção para termos e políticas.
    02.6 Separador Visual: Uma linha horizontal ou texto como "OU" para separar as opções de cadastro e login.

03. Seção de Login:
    03.1 Título: "Já possui uma conta? Faça login".
    03.2 Campos de E-mail e Senha.
    03.3 Botão de Login.
    03.4 Link para recuperação de senha.
    03.5 Texto para cadastro alternativo.

Considerações Finais: A tela de cadastro/login deve ser simples, clara e fácil de usar, minimizando a frustração do usuário. Utilize um design responsivo para que a tela fique bem em diferentes dispositivos. A inclusão de feedback visual, como mensagens de erro ou sucesso, pode ajudar a orientar o usuário durante o processo. Além disso, manter a estética alinhada com a identidade do FootUp vai proporcionar uma experiência coesa e profissional.
*/