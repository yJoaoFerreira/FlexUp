/*
Tela de Treinos

- Título da Tela:

    Um título claro como "Treinos" ou "Rotina de Treino" no cabeçalho da tela.

- Tela de Seleção de Rotina de Treino:

    Seção de Rotinas de Treino:

        Exibir uma lista ou grade de rotinas de treino disponíveis. Para cada rotina, incluir:

        Nome da Rotina: Título descritivo da rotina (ex.: "Treino de Dribles", "Aperfeiçoamento de Finalizações").

        Imagem ou Ícone: Representação visual da rotina.

        Duração Estimada: Indicação do tempo que o treino leva para ser completado.

        Dificuldade: Classificação do nível de dificuldade (ex.: Fácil, Médio, Difícil).

        Botão "Iniciar Treino": Um botão para iniciar a rotina selecionada.

- Opção para Treinos Programados ou Customizados:

    Seção de Treinos Programados:

        Listar os treinos que foram programados pelo usuário para dias específicos, com detalhes como:

            Data e Hora: Quando o treino está agendado.

            Tipo de Treino: Indicação se é um treino programado ou um desafio específico.

            Botão "Editar": Um botão que permite ao usuário ajustar a programação.

    Seção de Treinos Customizados:

        Um botão ou link que leva o usuário a uma tela de criação de treinos personalizados. Ao clicar, incluir opções para:

            Selecionar Exercícios: Permitir que o usuário escolha entre diferentes exercícios da biblioteca (ex.: dribles, finalizações, resistência).

            Definir Duração e Repetições: Campos para o usuário inserir a duração total do treino e o número de repetições desejadas para cada exercício.

            Salvar Treino: Um botão para salvar a rotina customizada e adicioná-la à lista de treinos.

- Registro de Desempenho ao Final de Cada Treino:

    Seção de Registro de Desempenho:

        Após a conclusão de um treino, apresentar uma tela de registro de desempenho. Incluir:

            Número de Repetições: Campo para o usuário inserir quantas repetições realizou para cada exercício.

            Tempo Total: Campo para registrar quanto tempo o treino levou.

            Observações: Uma área para o usuário adicionar notas sobre o que funcionou bem ou o que precisa ser melhorado.

            Botão "Salvar Desempenho": Um botão que salva as informações inseridas e as adiciona ao histórico do usuário.

- Layout Sugerido
    Cabeçalho: Logo do aplicativo e título "Treinos".

    Seção de Rotinas de Treino:

        Lista ou grade com rotinas de treino disponíveis, com ícones, nomes, durações e botões "Iniciar Treino".

    Seção de Treinos Programados:

        Lista de treinos agendados, com detalhes e botões "Editar".

    Seção de Treinos Customizados:

        Botão "Criar Novo Treino" que leva à tela de personalização.

    Seção de Registro de Desempenho (após o treino):

        Campos para inserir desempenho, com um botão "Salvar Desempenho".

Considerações Finais: A tela de treinos deve ser intuitiva e fácil de navegar, permitindo que os usuários escolham rapidamente entre rotinas programadas ou criem seus próprios treinos personalizados. O registro de desempenho deve ser um processo simples e eficiente, incentivando os usuários a documentar suas atividades e acompanhar seu progresso. Um design claro e visualmente atrativo, com ícones e informações de fácil leitura, ajudará a manter os usuários engajados e motivados em suas jornadas de treinamento no FootUp.
*/