/*
Tela de Perfil do Usuário

- Informações Pessoais:

    Título: Um título claro como "Meu Perfil" ou "Perfil do Usuário".

    Seção de Informações Pessoais:

        Foto de Perfil: Um espaço para exibir a foto de perfil do usuário. Um botão "Editar" que permite o upload de uma nova imagem.

        Nome do Usuário: Exibir o nome do usuário em destaque. Incluir um botão "Editar" ao lado, que leva a um campo de texto para alterar o nome.

        Nível: Um campo que mostra o nível atual do usuário (ex.: "Nível 5"). Incluir um breve texto explicativo sobre o que significa o nível e como ele é obtido.

        Botão de Conquistas: Um botão que leva a uma lista de conquistas que o usuário já obteve, permitindo uma rápida visualização.

- Estatísticas de Progresso:

    Título da Seção: "Estatísticas de Progresso".

    Experiência (XP):

        Exibir a quantidade total de XP acumulada pelo usuário, junto com uma barra de progresso que mostra quanto falta para o próximo nível.

    Gráficos de Desempenho:

        Gráficos Interativos: Incluir gráficos simples (como gráficos de barras ou de linhas) que representem o desempenho do usuário ao longo do tempo. Exemplos incluem:

            Evolução do XP ao longo das semanas.

        Número de treinos completados por semana.

            Desempenho em desafios diários.

        Conquistas Recentes:

            Listar as conquistas mais recentes com ícones representativos e datas em que foram obtidas.

- Opções para Editar Perfil e Preferências:

    Título da Seção: "Configurações do Perfil".

    Editar Perfil:

        Botão "Editar Perfil" que leva o usuário a uma tela onde ele pode alterar suas informações pessoais, como nome e foto de perfil.

    Preferências:

        Ajustes de Notificações: Um link ou botão que direciona o usuário para as configurações de notificações, permitindo que ele ajuste suas preferências.

        Privacidade: Um botão que leva a uma tela onde o usuário pode ajustar configurações de privacidade, como quem pode ver seu perfil ou suas conquistas.

        Botão de Sair: Um botão que permite ao usuário fazer logout da conta, se desejado.

- Layout Sugerido

    Cabeçalho: Logo do aplicativo e título "Meu Perfil".

    Seção de Informações Pessoais:

        Foto de perfil com botão de edição.

        Nome do usuário com botão de edição ao lado.

        Exibição do nível do usuário com descrição breve.

        Botão de conquistas que leva a uma lista de conquistas.

    Seção de Estatísticas de Progresso:

        Título: "Estatísticas de Progresso".

        Exibição do total de XP e barra de progresso.

        Gráficos interativos mostrando o desempenho ao longo do tempo.

        Lista das conquistas mais recentes.

    Seção de Configurações do Perfil:

        Título: "Configurações do Perfil".

        Botão "Editar Perfil".

        Link para ajustes de notificações e preferências.

        Botão de logout.

Considerações Finais: A tela de perfil do usuário deve ser clara e organizada, permitindo que o usuário visualize facilmente suas informações e progresso. Um design intuitivo, com feedback visual para alterações e estatísticas, contribuirá para uma experiência de uso mais envolvente. Adicionar elementos visuais, como ícones para estatísticas e conquistas, pode tornar a tela mais atraente. Além disso, a possibilidade de personalização e edição rápida de informações garantirá que o usuário se sinta no controle de sua experiência no FootUp.
*/